  <div class="col-md-12">丟向神轎的放炮方式，稱為犁炮，而後期也有直接使用香來當成引燃的工具。
    近期幾年在 <b><u>金垂髫鄉土文史協會</u></b> 的推廣之下，慢慢的又回復成使用犁頭生搭配火爐的古早式，
    在媽祖繞境的這幾天，協會也會在 <b>媽祖廟廣場前</b> 示範犁炮，而現場民眾也都有機會可以參與犁炮的活動喔！
    <br/>
    <br/>

    <h3>踩炮</h3> 即用腳踏鞭炮囉，一般會先在馬路上使用竹竿炮(連珠炮)鋪成鞭炮地毯，
    當神轎經過的同時一並引燃，踏著腳下引燃的鞭炮前進的方式稱為踩炮。
    <br/>
    <br/>

    <h3>炸轎</h3> 當然就是用"一大堆"的鞭炮(不只一、兩串)，來炸媽祖的神轎，
    這是目前北港吃炮常見的方式，所以也有人說這個就是"吃炮"，但當神轎下的那堆鞭炮引燃時，
    剎那間火光四飛，煙霧滿天，聲音之大有如雷聲，炸完的下一秒，天空就會下起炮屑雨來，
    當炮屑灑落一地時，四周自然的就會響起歡呼掌聲，所以北港人常會驕傲的說 <b>北港囝仔未驚炮</b>！
