    <h2>北港朝天宮</h2>為目前台灣最具影響力分靈眾多的媽祖廟之一，
    清康熙三十三年(西元1694年)，樹璧和尚自湄洲嶼朝天閣奉請媽祖來台，航途中遇暴風雨漂流至笨港(今北港)，
    樹璧和尚認為此為神意，遂將媽祖奉祀於笨港街，建天妃廟，後改名天后宮，再改今朝天宮，
    仿湄洲朝天閣故名「朝天宮」，起初僅以茅草建造，至<b>1700年</b>始建廟奉祀。
    <br/>
    <br/>
    <h2>傳說</h2>樹壁和尚將媽祖神像請至北港後，曾在一處古井下休息，也暫時將媽祖神像安置於古井上，
    不過後來當樹壁和尚要動身離開時，媽祖神像卻再也搬不動。
    後來經過請示，得知媽祖欲在此地建廟，而造就了今日的朝天宮。傳說今朝天宮鎮殿媽祖正下方，
    即為當年古井所在。
