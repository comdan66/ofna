    <h2>藝閣</h2>的「藝」指的是南管樂器彈唱的藝旦，而「閣」則指的是架子上臵放食物的木板，
    合在一起則有為藝術之閣、詩意之閣。
    <br/>
    <br/>
    <h2>一般</h2>扮演藝閣都會由十四歲以下的童男、童女，著古裝扮演戲劇角色。在民國四十年左右，
    由人工扛抬改為獸力，將「閣棚」裝臵於牛車上，由牛來拉動，
    後改以機動三輪車帶動，現在藝閣完全改用貨車車體，其外形比當年增大五倍之多！而視覺與藝術美感更勝以往！
